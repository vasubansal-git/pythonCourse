#for loop:

names = ["Vasu", "Sarthak", "Rekha", "Neeraj", "Vani"]
for name in names:
    print(name)

for name in names:
    print(name)
    for i in name:
        print(i)

for i in range(5):
    print(i)

for i in range(1, 10):
    print(i)

for i in range(1, 10, 2):
    print(i)

#reverse loop
for i in range(10, -1, -1):
    print(i)